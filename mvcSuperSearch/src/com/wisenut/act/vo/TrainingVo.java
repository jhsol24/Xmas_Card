package com.wisenut.act.vo;

public class TrainingVo {
	private String cfct_id;
	private String nm;
	private String cf_content;
	
	public String getCfct_id() {
		return cfct_id;
	}
	public void setCfct_id(String cfct_id) {
		this.cfct_id = cfct_id;
	}
	public String getNm() {
		return nm;
	}
	public void setNm(String nm) {
		this.nm = nm;
	}
	public String getCf_content() {
		return cf_content;
	}
	public void setCf_content(String cf_content) {
		this.cf_content = cf_content;
	}
}
