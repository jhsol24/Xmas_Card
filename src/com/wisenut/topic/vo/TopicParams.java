package com.wisenut.topic.vo;

public class TopicParams {
	private String CollId;
	private String NumKeywords;
	private String NumTopics;
	public String getCollId() {
		return CollId;
	}
	public void setCollId(String collId) {
		CollId = collId;
	}
	public String getNumKeywords() {
		return NumKeywords;
	}
	public void setNumKeywords(String numKeywords) {
		NumKeywords = numKeywords;
	}
	public String getNumTopics() {
		return NumTopics;
	}
	public void setNumTopics(String numTopics) {
		NumTopics = numTopics;
	}

	
}
